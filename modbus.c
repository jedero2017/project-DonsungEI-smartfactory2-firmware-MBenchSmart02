uint8_t uchCRCHi, uchCRCLo;
uint16_t uINDEX;

void mb_crc_compute(uint8_t rch)
{
    uINDEX = uchCRCHi ^ rch;
    uchCRCHi = uchCRCLo ^ auchCRCH[uINDEX];
    uchCRCLo = auchCRCL[uINDEX];
}

uint16_t modbus_get_crc(uint8_t * buf, uint16_t len)
{
    uint16_t d;
    uchCRCHi = uchCRCLo = 0xff;   // CRC 계산 start
    for (d = 0; d < len; d++)
    {
        mb_crc_compute(buf[d]);
    }
    d = (uchCRCHi << 8) + uchCRCLo;
    return d;
}
