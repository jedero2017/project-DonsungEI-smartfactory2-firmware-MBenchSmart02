bool modbus_read_input_reg(UART_Handle h_uart, int dev_no, int start_addr, int quantity, uint8_t* data)
{
    int recv_len;
    uint16_t crc16;
    int ret;

    int result_len;
    int i;

    //create packet
    modbus_send_buf[0] = (uint8_t)dev_no;
    modbus_send_buf[1] = 0x04;
    modbus_send_buf[2] = (uint8_t)((uint16_t)start_addr >> 8);
    modbus_send_buf[3] = (uint8_t)(start_addr & 0xFF);
    modbus_send_buf[4] = (uint8_t)((uint16_t)quantity >> 8);
    modbus_send_buf[5] = (uint8_t)(quantity & 0xFF);

    crc16 = modbus_get_crc(modbus_send_buf, 6);

    modbus_send_buf[6] = (uint8_t)(crc16 >> 8);
    modbus_send_buf[7] = (uint8_t)(crc16 & 0xFF);

    //send
    if(h_uart == modbus_pm_handle)
        GPIO_write(CONFIG_GPIO_PM485RDn, 1);
    else
        GPIO_write(CONFIG_GPIO_TM485RDn, 1);

    UART_write(h_uart, modbus_send_buf, 8);

    if(h_uart == modbus_pm_handle)
        GPIO_write(CONFIG_GPIO_PM485RDn, 0);
    else
        GPIO_write(CONFIG_GPIO_TM485RDn, 0);


    //receive
    recv_len = 0;
    result_len = 5 + quantity * 2;
    do
    {
        ret = UART_read(h_uart, &modbus_recv_buf[recv_len], sizeof(modbus_recv_buf));
        if(ret > 0)
            recv_len += ret;

        if(recv_len >= result_len)
            break;
    }
    while (ret > 0);

    //packet 검사
    if(recv_len != result_len)
        return false;

    if(modbus_recv_buf[0] != (uint8_t)dev_no)
        return false;

    if(modbus_recv_buf[1] != 0x04)
        return false;

    crc16 = modbus_get_crc(modbus_recv_buf, result_len - 2);

    if((uint8_t)((uint16_t)crc16 >> 8) != modbus_recv_buf[result_len-2])
        return false;

    if((uint8_t)(crc16 & 0xff) != modbus_recv_buf[result_len-1])
        return false;

    //copy data

    for(i = 0 ; i < quantity * 2 ; i++)
    {
        data[i] = modbus_recv_buf[3 + i];
    }

    return true;
}

